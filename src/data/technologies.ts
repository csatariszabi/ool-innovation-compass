function calcSust(mr:number,eb:number,ce:number,mf:number,sc:number,se:number,ev:number,trl:string): number {
  let w = mr*.20+eb*.20+ce*.15+mf*.15+sc*.15+se*.10+ev*.05;
  if (/TRL 1.2|TRL 2.3|TRL 2[^\d]/.test(trl)) w = Math.min(w, 2.0);
  else if (/TRL 1.3|TRL 1.4|TRL 3[^\d]/.test(trl)) w = Math.min(w, 2.8);
  return Math.round(w*100)/100;
}
function calcRepl(cr:number,pf:number,sc:number,md:number,ma:number,trl:string): number {
  let w = cr*.30+pf*.25+sc*.20+md*.15+ma*.10;
  if (/TRL 1.2|TRL 2.3|TRL 2[^\d]|TRL 1.3|TRL 3[^\d]|TRL 1.4/.test(trl)) w = Math.min(w, 2.8);
  return Math.round(w*100)/100;
}
function grade(s:number): 'A'|'B'|'C'|'D'|'E' {
  if (s>=4.0) return 'A';
  if (s>=3.2) return 'B';
  if (s>=2.4) return 'C';
  if (s>=1.6) return 'D';
  return 'E';
}
function mk(
  id:number, name:string, category:string, type:string, country:string, region:string,
  trl:string, established:number, diversity:string[],
  mr:number,eb:number,ce:number,mf:number,sc:number,se:number,ev:number,
  sVerdict:string, sGap:string,
  cr:number,pf:number,rsc:number,md:number,ma:number,
  replaces:string, rGap:string
): Technology {
  const sw = calcSust(mr,eb,ce,mf,sc,se,ev,trl);
  const rw = calcRepl(cr,pf,rsc,md,ma,trl);
  return {
    id, name, category, type, country, region, trl, established, diversity,
    sust: { mr,eb,ce,mf,sc,se,ev, weighted:sw, grade:grade(sw), verdict:sVerdict, gap:sGap },
    repl: { cr,pf,sc:rsc,md,ma, weighted:rw, grade:grade(rw), replaces, gap:rGap }
  };
}

